import { Toaster } from "@/components/ui/toaster";
import { Toaster as Sonner } from "@/components/ui/sonner";
import { TooltipProvider } from "@/components/ui/tooltip";
import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { BrowserRouter, Routes, Route } from "react-router-dom";
import Index from "./pages/Index";
import NotFound from "./pages/NotFound";
import RequestDetailsPage from "./pages/RequestDetailsPage";
import RequestDocumentsPage from "./pages/RequestDocumentsPage";
import AssignSalesRepPage from "./pages/AssignSalesRepPage";

const queryClient = new QueryClient();

const App = () => (
  <QueryClientProvider client={queryClient}>
    <TooltipProvider>
      <Toaster />
      <Sonner />
      <BrowserRouter>
        <Routes>
          <Route path="/" element={<Index />} />
          <Route path="/actions/request-details" element={<RequestDetailsPage />} />
          <Route path="/actions/request-documents" element={<RequestDocumentsPage />} />
          <Route path="/actions/assign-sales-rep" element={<AssignSalesRepPage />} />
          <Route path="*" element={<NotFound />} />
        </Routes>
      </BrowserRouter>
    </TooltipProvider>
  </QueryClientProvider>
);

export default App;
